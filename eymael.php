    <h2>JOSE MARIA EYMAEL</h2>
      <img src="img/eymael/eymael.jpg" class="rounded" width="304" height="236"></div>
      <p>Duas vezes candidato à prefeitura de São Paulo, o ex-deputado federal José Maria Eymael é o pré-candidato pelo PSDC à presidência da República. Esta será a sexta vez que o “democrata cristão” concorre ao posto de chefe do executivo do país. O bordão “um democrata cristão”, já conhecido pelo eleitor, foi apresentado por ele em 1985, quando se candidatou pela primeira vez à prefeitura de São Paulo, pelo extinto PDC. No ano seguinte, elegeu-se deputado federal pela sigla, e foi reeleito em 1990. É um dos fundadores do PSDC, e segue até hoje como presidente da legenda. Eymael foi candidato a presidente da República nas eleições de 1998, 2006, 2010, 2014 e 2016. E tentou a prefeitura de São Paulo uma segunda vez, em 2012. Advogado e empresário, Eymael é gaúcho, e atuou profissionalmente nas áreas do direito tributário e do marketing e comunicação. Em campanhas anteriores, afirmou que seu compromisso é com a família, na defesa de valores, e “com o atendimento pleno das suas necessidades, como emprego, educação, saúde, segurança e moradia”.</p>
