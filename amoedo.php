			<p>João Amoêdo é formado em administração de empresas e em engenharia e já atuou em diversas instituições financeiras. Atualmente é membro do Conselho de Administração da João Fortes. Amoêdo foi um dos fundadores do Partido Novo e presidiu a legenda de setembro de 2015 a junho deste ano. Ele deixou o cargo para poder concorrer nas eleições de 2018, já que o estatuto do partido proíbe que dirigentes partidários concorram a cargos eletivos. Entre as propostas que Amoêdo defende estão liberdades individuais, diminuição do papel do Estado e investimento em educação básica. O pré-candidato já disse ser favorável a programas sociais como o Bolsa Família, desde que tenham uma “porta de saída”. Amoêdo possui uma relação próxima com economistas das gestões do ex-presidente Fernando Henrique Cardoso, como os ex-presidentes do Banco Central Armínio Fraga e Gustavo Franco – este último deixou o PSDB para se filiar ao Partido Novo.</p>
