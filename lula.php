			<p>Lula era o candidato do PT nas Eleições 2018. Preso, o ex-presidente teve a candidatura negada pelo TSE com base na Lei da Ficha Limpa, deixando a chapa para Fernando Haddad e Manuela D'Ávila, respectivamente candidato a presidente e vice. Lula já foi presidente do Brasil por dois mandatos, de 2003 a 2010, e conseguiu eleger sua sucessora, a ex-presidente Dilma Rousseff (PT), por dois mandatos – o último acabou sendo interrompido pelo impeachment. Foi metalúrgico, sindicalista e um dos fundadores do Partido dos Trabalhadores (PT). O petista foi condenado a 9 anos e seis meses de prisão pelo juiz Sergio Moro e com a condenação confirmada em segunda instância pelo Tribunal Regional Federal da 4ª Região (TRF-4) pode ficar inelegível por causa da Lei Ficha Limpa. Lula ainda responde a outros seis processos em primeira instância – em Curitiba e em Brasília. Também há uma denúncia contra ele no Supremo Tribunal Federal (STF) aguardando análise dos ministros e outra em primeira instância, por obstrução de Justiça. As denúncias são de corrupção durante os anos em que esteve no poder e dizem respeito as operações Zelotes e Lava Jato.</p>
