		<h2>FERNANDO HADDAD</h2>
			<img src="img/haddad/haddad.jpg" class="rounded" width="304" height="236"></div>
			<p>Haddad é o candidato à presidência pelo PT nas Eleições 2018. Como Lula foi impedido de concorrer pela Justiça Eleitoral, ele assumiu a chapa, tendo Manuela D'Ávila como vice. Filho de libaneses nascido em São Paulo, Fernando Haddad é figura proeminente dentro dos quadros do PT. Advogado, formado pela Faculdade do Largo São Francisco, da USP, com especialização em Direito Civil, mestre em Economia e doutor em Filosofia, se filiou ao partido em 1983. Em 2001, aceitou o convite da então petista e prefeita de São Paulo, Marta Suplicy, e foi nomeado subsecretário de Finanças e Desenvolvimento Econômico da capital paulista. Ficou no cargo até 2003 quando foi convocado pelo à época ministro de Planejamento e Finanças, Guido Mantega, para ser assessor especial da pasta. O trabalho junto ao ministério da Educação no governo federal começou em 2004, ao ser convidado pelo ministro da pasta, Tarso Genro, para assumir o cargo de secretário-executivo. Com a saída de Genro para assumir a presidência do PT, Haddad herdou o cargo, no qual ficou até 2012, quando se candidatou à prefeitura de São Paulo. Venceu José Serra (PSDB) no segundo turno, em eleição que começou tensa para o partido: após aceitar o apoio de Paulo Maluf (PP), inimigo tradicional do partido, Haddad viu sua candidata a vice, a ex-prefeita de São Paulo e hoje deputada federal pelo PSOL, Luiza Erundina, deixar a chapa. Foi substituída por Nádia Campeão (PCdoB). Candidato à reeleição em 2016, foi derrotado ainda no primeiro turno para João Dória (PSDB). Pela provável interdição à candidatura de Lula à presidência, é visto por muitos dentro da legenda como o substituto imediato.</p>
