			<p>Maria Osmarina Marina Silva Vaz de Lima, 59 anos, nasceu num seringal do Acre. É filha de um seringueiro e uma dona de casa. Viveu até os 15 anos numa palafita. Mudou-se para a área urbana de Rio Branco, e trabalhou de empregada doméstica. Alfabetizou-se aos 16 anos. Formou-se em História e trabalhou como professora. Teve inúmeros problemas de saúde: malária, contaminação por mercúrio, leishmaniose, hepatite. Chegou a correr risco de morte em 1997. Após sobreviver a esse episódio, virou evangélica, deixando o catolicismo. Ajudou a fundar a CUT no Acre. Em 1986, filiou-se ao PT. Elegeu-se vereadora (1988), deputada estadual (1990) e senadora (1994, 2002 e 2008). Licenciou-se do Senado em 2003, quando assumiu o Ministério do Meio Ambiente do governo Lula. Deixou a pasta em 2008 por divergências com Dilma Rousseff, então ministra da Casa Civil. Em 2009 migrou para o PV, sigla pela qual concorreu à Presidência em 2010, ficando em 3.º lugar. Em 2014, disputou novamente o Planalto pelo PSB, repetindo a 3.ª posição. Agora está na Rede, o partido que fundou. É casada pela 2.ª vez e tem 4 filhos.</p>
