			<p>Ciro Ferreira Gomes, de 60 anos, nasceu em Pindamonhangaba (SP), mas mudou-se ainda criança para Sobral (CE). É formado em Direito pela Universidade Federal do Ceará e também cursou Economia na Harvard Law School. Construiu sua carreira política como deputado estadual (1983-1988), prefeito de Fortaleza (1989-1990) e governador do Ceará (1991-1994). Em Brasília foi ministro da Fazenda no governo Itamar Franco (1994–1995) em plena implantação do Plano Real e ministro da Integração Nacional no governo Lula (2003–2006). Também exerceu um mandato de deputado federal (2007-2010). Já passou por sete partidos ao longo de 37 anos de vida pública. Começou no PDS, que sucedeu a Arena, legenda que dava sustentação ao regime militar. Depois passou por PMDB e ajudou a fundar o PSDB ao lado de Tasso Jereissati. No ninho tucano elegeu-se prefeito e governador do estado. Migrou mais tarde para o PPS, partido pelo qual concorreu à Presidência da República em duas eleições, em 1998 e 2002 – nesta última fez mais de 10 milhões votos, mas terminou em quarto lugar. Em 2005 filiou-se ao PSB, depois passou Prona e desde 2015 está no PDT.</p>
