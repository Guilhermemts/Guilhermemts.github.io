			<p>Filho do ex-presidente João Goulart, deposto pela ditadura militar em 1964, João Vicente Goulart é pré-candidato a presidente pelo Partido Pátria Livre (PPL). João Vicente é filósofo e tem dedicado a vida à memória do pai – é presidente do instituto que leva o nome de Jango e escreveu um livro sobre a vida no exílio do ex-presidente. Exerceu apenas um mandato eleitoral, como deputado estadual pelo PDT do Rio Grande do Sul, em 1982. Também associa ao pai a plataforma de sua candidatura e fala em relançar as “reformas de base”, conjunto de medidas consideradas “socializantes” propostas por Jango nos anos 1960. O partido de João Vicente tem origem no antigo MR-8 (Movimento Revolucionário 8 de Outubro), grupo que defendeu a luta armada logo após a ditadura, mas que, a partir dos anos 1980, virou braço auxiliar do MDB, e depois do PMDB. O presidente nacional da legenda é o ex-guerrilheiro Sérgio Rubens.</p>
