			<p>O tucano Geraldo Alckmin é um nome conhecido dos brasileiros. O médico seguiu uma carreira tradicional na política: costuma brincar que entrou no ramo “por acaso”, quando foi eleito para o cargo de vereador pelo MDB, em Pindamonhangaba, em 1972. De lá para cá, ele não saiu mais da vida pública. Foi prefeito, deputado estadual e deputado federal, cargo que ocupava quando ‘trocou de partido’ – ele foi um dos fundadores do PSDB. No Legislativo, ele foi o autor do projeto de lei que acabou virando o Código de Defesa do Consumidor. Em 1994, ele era o vice da chapa eleita para o governo do estado de São Paulo, comandada por Mário Covas. A dupla foi reeleita em 1998. Em 2001 precisou assumir o governo paulista por causa da morte precoce de Covas. Foi governador do estado até 2006, quando abriu mão do cargo para disputar a presidência pela primeira vez. Alckmin foi derrotado no segundo turno que disputou com o petista Luiz Inácio Lula da Silva, na época do escândalo do mensalão. Ele ainda sofreu outras duas derrotas para cargos do Executiva: perdeu a disputa da prefeitura de São Paulo em 2000 e em 2008. Desde 2010, é o governador de São Paulo. Discreto, seu nome é envolvido em poucas polêmicas. Recentemente, foi citado na delação da Odebrecht como beneficiário de R$ 10 milhões em caixa 2. O STJ também recebeu um inquérito sigiloso contra o tucano, aberto pelo Ministério Público Federal.</p>
