			<p>Presidente do Banco Central nos oito anos de governo Lula e ministro da Fazenda desde a posse de Michel Temer, Henrique Meirelles espera se credenciar como responsável pela recuperação da economia. Na época da eleição, o país deve ter PIB em alta, inflação relativamente controlada e taxa básica de juros em níveis historicamente baixos. Mas o desemprego, embora diminuindo, ainda estará elevado, com perto de 10 milhões de pessoas sem ocupação, o que pode enfraquecer o discurso de Meirelles. Admitindo-se “presidenciável”, ele tenta ganhar visibilidade: aproximou-se dos evangélicos e contratou uma equipe para filmar seus compromissos públicos. Mas carregará o peso de tentar implementar medidas amargas como a reforma da Previdência. Bem visto no mercado financeiro, Meirelles não é exatamente popular entre o eleitorado. Parte de um patamar baixo nas pesquisas de intenção de voto (2%) e espera chegar a 5% até março, quando terá de deixar o cargo se decidir concorrer à Presidência.</p>
