			<p>Jair Bolsonaro está em seu sétimo mandato como deputado federal. Militar da reserva, ganhou espaço no cenário nacional ao manifestar posições conservadoras e críticas ao comunismo, à esquerda e ao politicamente correto. O parlamentar defende o período da ditadura militar brasileira e é contra a divulgação de documentos da época. Bolsonaro adotou recentemente um discurso liberal na economia, com a defesa das privatizações e adoção de políticas ortodoxas. Com pouco conhecimento na área, tem reafirmado que entregará os rumos econômicos para o economista Paulo Guedes, que possui Ph. D pela Universidade de Chicago e foi um dos fundadores do Instituto Millenium. O candidato à presidência é uma das vozes que denuncia a ideologia de gênero e a doutrinação nas escolas e é contra cotas raciais nas universidades. Defende o homeschooling e o porte de arma para toda a população. Caso eleito, o candidato do PSL afirma que dará espaço para nomes técnicos e para as Forças Armadas nos ministérios.</p>
