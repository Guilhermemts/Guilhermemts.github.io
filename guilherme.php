			<p>Natural de São Paulo (SP), Guilherme Castro Boulos é coordenador nacional do Movimento dos Trabalhadores Sem Teto (MTST), movimento popular que atua em grandes cidades com objetivo de ocupar imóveis, no que chamam de “reforma urbana”. É filho de uma família abastada. Seu pai é um dos mais renomados infectologistas do país, Marcos Boulos, professor de Medicina na Universidade de São Paulo (USP). O candidato de esquerda é graduado em Filosofia pela USP, tem especialização em Psicologia Clínica pela PUC-SP e mestrado em Psiquiatria pela USP. Na adolescência, se envolveu com o movimento estudantil da União da Juventude Comunista. Aos 19 anos, saiu de casa para viver em um acampamento de sem-teto. Anos depois, se tornou um dos organizadores do MTST. Ganhou destaque nacionalmente ao defender a soltura do ex-presidente Lula, que está preso em Curitiba após ser condenado por corrupção. Boulos é a favor da legalização do aborto, defende o aumento de impostos para a população de maior renda e a desmilitarização</p>
