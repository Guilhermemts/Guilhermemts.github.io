	<p>O partido Patriota escolheu o deputado federal Cabo Daciolo (RJ) para ser o candidato da legenda na disputa pela Presidência da República. Ele foi lançado pré-candidato pelo Patriota em março, como alternativa encontrada para substituir Jair Bolsonaro, que por algum tempo foi tido como estrela do partido, embora não tenha chegado a se filiar. Daciolo é conhecido por controvérsias. Em setembro de 2017, então no PTdoB do Rio de Janeiro, o deputado defendeu em discurso o fechamento do Congresso e uma intervenção militar no país. Em março deste ano, ao ser lançado pré-candidato pelo Patriota, disse à Gazeta do Povo que não retirava “uma vírgula” do que falou sobre o fechamento do Congresso e afirmou ainda que a Câmara era dominada por uma quadrilha. No mês passado, subiu à tribuna do Plenário para profetizar a cura de uma colega. O deputado foi um dos políticos que ganhou projeção nas redes sociais com a greve dos caminhoneiros, em maio deste ano, ao apoiar um dos líderes do movimento, o motorista Wallace Landim, conhecido como “Chorão”. Sargento licenciado do Corpo de Bombeiros, o deputado também teve destaque como líder do movimento grevista de integrantes da corporação no Rio de Janeiro em 2012. Está no seu primeiro mandato como deputado federal eleito pelo Rio de Janeiro. É casado e pai de três filhos.</p>
