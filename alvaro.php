	<p>Com quase meio século de vida pública, o senador paranaense Alvaro Dias (Podemos) tenta pela segunda vez se viabilizar como candidato a presidente. Na primeira, em 1989, foi pré-candidato pelo PMDB e acabou preterido por Ulysses Guimarães. Antes de chegar ao Senado, foi deputado estadual, federal e governador do Paraná (1987-1991). Após passar por seis diferentes legendas (PMDB, PST, PP, PSDB, PDT e PV), participou em 2017 da criação do Podemos, partido que nasce da renomeação do PTN, de 1945. Alvaro ganhou fama como um dos mais ferrenhos opositores às gestões Lula e Dilma Rousseff e pela atuação como líder do PSDB e da oposição. Teve destaque durante a CPI dos Correios, que investigou o mensalão, e a CPI do Futebol. Um dos motivos para deixar o PSDB foi o descontentamento com a impossibilidade de realização de prévias internas para a escolha de candidatos a presidente pela sigla. Tem se dedicado a atrair personalidades de peso, com os ex-jogadores Romário, que também é senador pelo Rio de Janeiro, e Bebeto.</p>
